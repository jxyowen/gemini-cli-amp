import fs from 'fs';
import path from 'path';
import yaml from 'js-yaml';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.join(__dirname, '..');

// 读取 YAML 文件
const yamlPath = path.join(root, 'packages/core/src/core/apiJsonSchema.yml');
const outputPath = path.join(root, 'packages/core/src/core/apiJsonSchema.generated.ts');

try {
  console.log('Converting YAML to TypeScript...');
  
  // 读取并解析 YAML 文件
  const yamlContent = fs.readFileSync(yamlPath, 'utf8');
  const jsonData = yaml.load(yamlContent);
  
  // 生成 TypeScript 文件内容
  const tsContent = `/**
 * @license
 * Copyright 2025 Google LLC
 * SPDX-License-Identifier: Apache-2.0
 */

// This file is auto-generated from apiJsonSchema.yml
// Do not edit this file directly - edit the YAML file instead

export const apiJsonSchema = ${JSON.stringify(jsonData, null, 2)} as const;

export default apiJsonSchema;
`;

  // 写入 TypeScript 文件
  fs.writeFileSync(outputPath, tsContent, 'utf8');
  
  console.log(`✅ Successfully generated ${outputPath}`);
} catch (error) {
  console.error('❌ Error generating API schema:', error);
  process.exit(1);
} 